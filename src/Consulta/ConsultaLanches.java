    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        Lanche consultaLanche = new Lanche();
        consultaLanche.setNome_lanche(txtNomeLanche.getText());
        consultaLanche.setDesc_lanche(txtDescLanche.getText());
        consultaLanche.setPreco_lanche(txtPrecoLanche.getText());
        if (txtCodLanche.getText().trim().equalsIgnoreCase("")) {
            consultaLanche.setCodigo_lanche(0);
        } else {
            consultaLanche.setCodigo_lanche(Integer.valueOf(txtCodLanche.getText()));
        }
        
        LanchesDAO ld = new LanchesDAO();
        Lanche retorno = ld.consulta(consultaLanche);
        
        if(retorno != null){
            lblMsg.setText("Lanche encontrado com sucesso");
            txtCodLanche.setText(String.valueOf(retorno.getCodigo_lanche()));
            txtNomeLanche.setText(retorno.getNome_lanche());
            txtDescLanche.setText(retorno.getDesc_lanche());
            txtPrecoLanche.setText(retorno.getPreco_lanche());
        }else{
            lblMsg.setText("Lanche não encontrado");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

