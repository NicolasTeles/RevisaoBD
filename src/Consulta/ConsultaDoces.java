    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        Doce consultaDoce = new Doce();
        consultaDoce.setNome_doce(txtNomeDoce.getText());
        consultaDoce.setDesc_doce(txtDescDoce.getText());
        consultaDoce.setPreco_doce(txtPrecoDoce.getText());
        if (txtCodDoce.getText().trim().equalsIgnoreCase("")) {
            consultaDoce.setCodigo_doce(0);
        } else {
            consultaDoce.setCodigo_doce(Integer.valueOf(txtCodDoce.getText()));
        }
        
        DocesDAO dd = new DocesDAO();
        Doce retorno = dd.consulta(consultaDoce);
        
        if(retorno != null){
            lblMsg.setText("Doce encontrado com sucesso");
            txtCodDoce.setText(String.valueOf(retorno.getCodigo_doce()));
            txtNomeDoce.setText(retorno.getNome_doce());
            txtDescDoce.setText(retorno.getDesc_doce());
            txtPrecoDoce.setText(retorno.getPreco_doce());
        }else{
            lblMsg.setText("Doce não encontrado");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

