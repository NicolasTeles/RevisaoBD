    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String SQL = "DELETE FROM nicolas_teles_lanchonete.doces WHERE codigo_doce = "+txtCodDoce.getText();
            Connection minhaConexao = Conexao.getConexao();
            PreparedStatement comando = minhaConexao.prepareStatement(SQL);
            
            int retornar = comando.executeUpdate();
            if(retornar>0){
                JOptionPane.showMessageDialog(null, "Doce "+txtNomeDoce.getText()+" deletado com sucesso");
            }else{
                JOptionPane.showMessageDialog(null, "Erro ao deletar o doce");
            }
        } catch (SQLException ex) {
            Logger.getLogger(ConsultaDoces.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

