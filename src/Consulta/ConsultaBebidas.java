    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String SQL = "DELETE FROM nicolas_teles_lanchonete.bebidas WHERE codigo_bebida = "+txtCodBebida.getText();
            Connection minhaConexao = Conexao.getConexao();
            PreparedStatement comando = minhaConexao.prepareStatement(SQL);
            
            int retornar = comando.executeUpdate();
            if(retornar>0){
                JOptionPane.showMessageDialog(null, "Bebida "+txtNomeBebida.getText()+" deletada com sucesso");
            }else{
                JOptionPane.showMessageDialog(null, "Erro ao deletar a bebida");
            }
        } catch (SQLException ex) {
            Logger.getLogger(ConsultaBebidas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

