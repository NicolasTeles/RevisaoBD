    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        Bebida consultaBebida = new Bebida();
        consultaBebida.setNome_bebida(txtNomeBebida.getText());
        consultaBebida.setDesc_bebida(txtDescBebida.getText());
        consultaBebida.setPreco_bebida(txtPrecoBebida.getText());
        if (txtCodBebida.getText().trim().equalsIgnoreCase("")) {
            consultaBebida.setCodigo_bebida(0);
        } else {
            consultaBebida.setCodigo_bebida(Integer.valueOf(txtCodBebida.getText()));
        }
        
        BebidasDAO bd = new BebidasDAO();
        Bebida retorno = bd.consulta(consultaBebida);
        
        if(retorno != null){
            lblMsg.setText("Bebida encontrada com sucesso");
            txtCodBebida.setText(String.valueOf(retorno.getCodigo_bebida()));
            txtNomeBebida.setText(retorno.getNome_bebida());
            txtDescBebida.setText(retorno.getDesc_bebida());
            txtPrecoBebida.setText(retorno.getPreco_bebida());
        }else{
            lblMsg.setText("Bebida não encontrada");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

